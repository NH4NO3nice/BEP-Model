MODULE module_initialization
    contains
    
    !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
    !!!!!!!!!!!!!!!!!!!!!!!! 地转风初始化，由位势高度场计算初始地转风 !!!!!!!!!!!!!!!!!!!!!!!!!
    !!!!!!!!!!!!!!!! 内部网格采用中央差格式，边界网格采用前插or后差格式 !!!!!!!!!!!!!!!!
    !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
    SUBROUTINE geostrophic_wind_centre(ua, va, za, m, f, nx, ny, d)
    ! n-1时刻的初始高度场、风场ua,va,za
    ! 地图放大系数m
    ! 地转参数f
    ! 区域格点nx,ny
    ! 网格距d
    ! 重力加速度g
        IMPLICIT NONE
    
        INTEGER, PARAMETER :: g = 9.8
        INTEGER :: i, j
        INTEGER :: nx, ny
        REAL :: ua(nx, ny), va(nx, ny), za(nx, ny)
        REAL :: m(nx, ny), f(nx, ny)
        REAL :: d

        !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!! 计算ua!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
        do j = 1, ny
            if (j == 1) then  !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!! 下边界，前插
                do i = 1, nx
                        ua(i, 1) = (-1 * m(i, 1) * g / f(i, 1)) * ((za(i, 2) - za(i, 1)) / d)
                enddo
            else if (j > 1 .and. j < ny) then  !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!! 中间，中央差
                do i = 1, nx
                    ua(i, j) = (-1 * m(i, j) * g / f(i, j)) * ((za(i, j + 1) - za(i, j - 1)) / (2.0 * d))
                enddo
            else  !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!! 上边界，后插
                do i = 1, nx
                    ua(i, ny) = (-1 * m(i, ny) * g / f(i, ny)) * ((za(i, ny) - za(i, ny - 1)) / d)
                enddo
            endif
        enddo
        
        !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!! 计算va!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
        do i = 1, nx
            if (i == 1) then  !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!! 左边界，前插
                do j = 1, ny
                        va(1, j) = (m(1, j) * g / f(1, j)) * ((za(2, j) - za(1, j)) / d)
                enddo
            else if (i > 1 .and. i < nx) then  !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!! 中间，中央差
                do j = 1, ny
                    va(i, j) = (m(i, j) * g / f(i, j)) * ((za(i + 1, j) - za(i - 1, j)) / (2.0 * d))
                enddo
            else  !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!! 右边界，后插
                do j = 1, ny
                    va(nx, j) = (m(nx, j) * g / f(nx, j)) * ((za(nx, j) - za(nx - 1, j)) / d)
                enddo
            endif
        enddo
        return
    END SUBROUTINE geostrophic_wind_centre
    
    !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
    !!!!!!!!!!!!!!!!!!!!!!!! 地转风初始化，由位势高度场计算初始地转风 !!!!!!!!!!!!!!!!!!!!!!!!!
    !!!!!!!!!!!!!!!!!!!!!!!! 采用前插格式，右边界和上边界采用后插格式 !!!!!!!!!!!!!!!!!!!!!!!!!
    !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
