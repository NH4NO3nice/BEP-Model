        REAL :: m(nx, ny), f(nx, ny)
        REAL :: d

        !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!! 计算ua!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
        do j = 1, ny
            if (j < ny) then  !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!  前插
                do i = 1, nx
                        ua(i, j) = (-1 * m(i, j) * g / f(i, j)) * ((za(i, j + 1) - za(i, j)) / d)
                enddo
            else  !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!! 上边界，后插
                do i = 1, nx
                    ua(i, ny) = (-1 * m(i, ny) * g / f(i, ny)) * ((za(i, ny) - za(i, ny - 1)) / d)
                enddo
            endif
        enddo
        
        !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!! 计算va!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
        do i = 1, nx
            if (i < nx) then  !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!  前插
                do j = 1, ny
                        va(i, j) = (m(i, j) * g / f(i, j)) * ((za(i + 1, j) - za(i, j)) / d)
                enddo
            else  !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!! 右边界，后插
                do j = 1, ny
                    va(nx, j) = (m(nx, j) * g / f(nx, j)) * ((za(nx, j) - za(nx - 1, j)) / d)
                enddo
            endif
        enddo
        return
    END SUBROUTINE geostrophic_wind_forward
    
    !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
    !!!!!!!!!!!!!!!!!!!!!!!! 地转风初始化，由位势高度场计算初始地转风 !!!!!!!!!!!!!!!!!!!!!!!!!
    !!!!!!!!!!!!!!!!!!!!!!!! 采用后插格式，左边界和下边界采用后插格式 !!!!!!!!!!!!!!!!!!!!!!!!!
